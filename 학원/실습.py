# ============================================================
# 3주차 강의자료 ④: 연산자 오버로딩 (Operator Overloading)
# ============================================================
# 같은 연산자 +가 타입에 따라 다르게 동작하는 것도 다형성!
#   3 + 4        → 7        (숫자 덧셈)
#   "Hello" + "!" → "Hello!" (문자열 결합)
#   [1,2] + [3]  → [1,2,3]  (리스트 결합)
# ============================================================


# ── 우리만의 + 연산자 정의: Vector 클래스 ────────────────────

class Vector:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def __add__(self, other):
        """+ 연산자를 사용하면 이 메서드가 호출됨"""
        return Vector(self.x + other.x, self.y + other.y)

    def __str__(self):
        """print()로 출력할 때 호출됨"""
        return f"Vector({self.x}, {self.y})"


# ── 테스트 ───────────────────────────────────────────────────

v1 = Vector(1, 2)
v2 = Vector(3, 4)
v3 = v1 + v2       # __add__가 호출됨!

print(v1)           # Vector(1, 2)
print(v2)           # Vector(3, 4)
print(v3)           # Vector(4, 6)

# ── 정리 ─────────────────────────────────────────────────────
# __add__  → +    연산자
# __sub__  → -    연산자
# __mul__  → *    연산자
# __eq__   → ==   연산자
# __lt__   → <    연산자
# __str__  → print() 출력
# __len__  → len() 함수
